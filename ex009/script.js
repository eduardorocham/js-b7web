
function dia(dia) {

    let diaNome = '';

switch (dia) {
    case 1:
        diaNome = 'Domingo';
        break
    case 2:
        diaNome = 'Segunda-feira';
        break
    case 3: 
        diaNome = 'Terça-feira';
        break
    case 4:
        diaNome = 'Quarta-Feira';
        break
    case 5:
        diaNome = 'Quinta-feira';
        break
    case 6:
        diaNome = 'Sexta-feira';
        break
    case 7:
        diaNome = 'Sábado'
        break
    }

    console.log('Hoje é '+diaNome+'!')
}
